public class PrintArray {
      public void printArrElement(String[] arr) {
         for (int i = 0; i < arr.length; i++) {
             System.out.println(i + "." + 1 + arr[i]);
         }
     }

         public void printArrElement(int[] arr) {
            for (int i = 0; i < arr.length; i++) {
                System.out.println(i + "." + 1 + arr[i]);
            }
        }

    }
